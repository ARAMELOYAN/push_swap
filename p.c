#include "push_swap.h"

void	p(char *str, t_list **a, t_list **b)
{
	t_list	*temp;

	if (*b)
	{
		temp = *b;
		*b = (*b)->next;
		ft_lstadd_front(a, temp);
		ft_printf("%s\n", str);
	}
}
